        + " VALUES ('"+txtNama.getText() +"',"
        + "'"+txtUser.getText()+"',"
        + "'"+jPass.getText()+"',"
        + "'"+txtEmel.getText()+"',"
        + "'"+jDate.getText()+"',"
        + "'"+txtCategory.getText()+"')";
