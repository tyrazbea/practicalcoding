    private void BtnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCariActionPerformed
        String sqlcari="SELECT * FROM maklumat_pelajar WHERE NamaPengguna='"+txtcarian.getText()+"'LIMIT 1";
        try{
            rs=st.executeQuery(sqlcari);
            rs.last();
            int kiraRow=rs.getRow();

            if(kiraRow>0){
                JOptionPane.showMessageDialog(null, "Found ");
                txtNama.setText(rs.getString(2));
                txtUser.setText(rs.getString(3));
                jPass.setText(rs.getString(4));
                txtEmel.setText(rs.getString(5));
                jDate.setText(rs.getString(6));
                txtCategory.setText(rs.getString(7));
            }
            else{
                JOptionPane.showMessageDialog(null, "Try Again:( ");
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_BtnCariActionPerformed

